#define FNV_prime 16777619

inline unsigned long fnv_hash (const char *p, int s)
{
   unsigned long h = 2166136261UL;	/* FNV-1a hash */
   int i = 0;

   for (; i < s; i++)
	   h = ((h ^ p[i]) * FNV_prime);

   return h;
}


